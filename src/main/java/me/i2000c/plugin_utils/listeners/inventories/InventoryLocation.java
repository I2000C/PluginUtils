package me.i2000c.plugin_utils.listeners.inventories;

/**
 * Represents the clicked inventory in a CustomInventoryClickEvent
 */
public enum InventoryLocation{
    /**
     * Clicked on the upper inventory
     */
    TOP,
    
    /**
     * Clicked on the bottom inventory
     */
    BOTTOM,
    
    /**
     * Clicked outside all inventores
     */
    NONE;
}
